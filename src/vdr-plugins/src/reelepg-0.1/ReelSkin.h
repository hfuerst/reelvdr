#define IMGPATH(osd, imgName, img, themePath, fd) sprintf(m_path, "%s/%s", themePath, img); \
    fd = open(m_path, O_RDONLY); \
    if(fd == -1) {\
        sprintf(m_path, "%s/%s", mUseExtSkin ? DEFAULT_NG_IMG_PATH : DEFAULT_IMG_PATH, img); \
        fd = open(m_path, O_RDONLY); \
        if(fd == -1) \
            sprintf(m_path, "%s/%s", DEFAULT_OPT_PATH, img); \
	else \
	    close(fd); \
    } else \
        close(fd); \
    osd->SetImagePath(imgName, m_path); 

